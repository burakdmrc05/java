    private void btnSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSilActionPerformed
        // TODO add your handling code here:
        
        lblMesaj.setText("");
        int secili=calisanTabloso.getSelectedRow();
        if(secili==-1){
            
            if(model.getRowCount()==0){
                
                lblMesaj.setText("tabloda eleman yok");
            }
            else{
                lblMesaj.setText("secili eleman yok");
                        
            }
        }
        else{
            int id=(int) model.getValueAt(secili, 0);
            islemler.sil(id);
            calisanGoruntule();
            lblMesaj.setText("Silme işlemi tamamlandı");
            txtAd.setText("");
            txtSoyad.setText("");
            txtMaas.setText("");
            txtDept.setText("");
                 
        }
                
    }//GEN-LAST:event_btnSilActionPerformed

    /**
     * @param args the command line arguments
     */
    public void calisanGoruntule(){
         model.setRowCount(0);
        
        ArrayList<Calisan> calisanlar = new ArrayList<Calisan>();
        
        
        calisanlar = islemler.calisanlariGetir();
        
        if (calisanlar != null ) {
            
            for (Calisan calisan : calisanlar) {
                Object[] eklenecek = {calisan.getId(),calisan.getAd(),calisan.getSoyad(),calisan.getDepartman(),calisan.getMaas()};
                
                model.addRow(eklenecek);
                 
            }
            
        }
        
        
    }
