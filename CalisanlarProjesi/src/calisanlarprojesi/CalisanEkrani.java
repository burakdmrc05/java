    private void btnGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuncelleActionPerformed
        lblMesaj.setText("");
        String ad=txtAd.getText();
        String soyad=txtSoyad.getText();
        String maas=txtMaas.getText();
        String dept=txtDept.getText();
        
        int secili=calisanTabloso.getSelectedRow();
        if(secili==-1){
            if(model.getRowCount()==0){
                lblMesaj.setText("Calısan tablosu su anda bos");
            }
            else{
            lblMesaj.setText("Secili eleman yok");
            }
        }
        else{
            int id=(int) model.getValueAt(secili, 0);
            islemler.guncelle(id,ad,soyad,maas,dept);
            calisanGoruntule();
            lblMesaj.setText("Güncelleme yapıldı");
        }
        
    }//GEN-LAST:event_btnGuncelleActionPerformed

